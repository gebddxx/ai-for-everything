function toSimp(text) {
  let result = '';
  for (const ch of text) {
    result += t2s[ch] || ch;
  }
  return result;
}

const fs = require('fs');
const pages = fs.readdirSync('src/pages').filter(f => f.endsWith('Overview.tsx') || f.endsWith('Tutorial.tsx'));
let total = 0;

for (const p of pages) {
  const path = 'src/pages/' + p;
  let content = fs.readFileSync(path, 'utf8');
  const original = content;

  // Find all L('A','B','C') and convert arg2 (zh-CN) to simplified
  let changes = 0;
  content = content.replace(/L\('([^']*)','([^']*)','([^']*)'\)/g, (match, a, b, c) => {
    const simp = toSimp(b);
    if (simp !== b) { changes++; total++; }
    return `L('${a}','${simp}','${c}')`;
  });

  if (content !== original) {
    fs.writeFileSync(path, content);
    console.log(p + ': ' + changes + ' trad chars converted in zh-CN position');
  }
}

console.log('Total traditional chars converted: ' + total);
